    private Color getColor(int num){
        switch(num%9){
            case 0:
            return Color.BLUE;
              case 1:
            return Color.CYAN;
              case 2:
            return Color.GREEN;
              case 3:
            return Color.MAGENTA;
              case 4:
            return Color.ORANGE;
              case 5:
            return Color.PINK;
              case 6:
            return Color.RED;
              case 7:
            return Color.YELLOW;
              case 8:
            return Color.lightGray;
              
            default:
            return Color.BLACK;
        }
    }

