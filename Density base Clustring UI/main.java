    public void readFile(){
          
         try {
            
           
             
             
             br=new BufferedReader(new InputStreamReader(getClass().getResourceAsStream("/khedmat1/sampleData.pos")));
             
            while(br.ready()){
                 double[] po=new double[3];
               String[] buffer=br.readLine().split(",");
               for(int i=0;i<buffer.length;i++){
                   po[i]=Double.valueOf(buffer[i]);
               } 
               pointList.add(new DoubleArray(po));
                        }
       
       
             
          
        } catch (Exception e){
        }
    }
    
    
    
   
    
